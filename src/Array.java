public class Array {
    public static void main(String[] args) throws Exception {
        int[] numbers = new int[5];

        numbers[0] = 5;
        numbers[1] = 10;
        numbers[2] = 15;
        numbers[3] = 20;
        numbers[4] = 25;

        System.out.println(numbers[4]);

        String[] names = new String[3];

        names[0] = "John";
        names[1] = "Mary";
        names[2] = "Bob";

        System.out.println(names[2]);
    }
}
